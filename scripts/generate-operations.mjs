import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const sdkDir = path.resolve(scriptDir, "..");
const specPath = path.resolve(sdkDir, "openapi/openapi.yaml");
const outputDir = path.resolve(sdkDir, "src/generated");
const metadataPath = path.resolve(outputDir, "operation-metadata.ts");
const indexPath = path.resolve(outputDir, "index.ts");

const source = await readFile(specPath, "utf8");
const document = YAML.parse(source);

const surfaceEntries = {
  management: [],
  runtime: [],
  system: []
};
const metadataEntries = [];

for (const [pathname, pathItem] of Object.entries(document.paths ?? {})) {
  if (!pathItem || typeof pathItem !== "object") {
    continue;
  }

  for (const [method, operation] of Object.entries(pathItem)) {
    if (!operation || typeof operation !== "object") {
      continue;
    }

    const operationId = operation.operationId;

    if (!operationId) {
      continue;
    }

    const auth = inferAuth(operation.security);
    const surface = auth === "bearer" ? "management" : auth === "license" ? "runtime" : "system";
    const successEntries = extractSuccessResponses(operation.responses ?? {});

    metadataEntries.push({
      operationId,
      method: method.toUpperCase(),
      path: pathname,
      auth,
      successEntries
    });
    surfaceEntries[surface].push(operationId);
  }
}

metadataEntries.sort((left, right) => left.operationId.localeCompare(right.operationId));
surfaceEntries.management.sort();
surfaceEntries.runtime.sort();
surfaceEntries.system.sort();

await mkdir(outputDir, { recursive: true });
await writeFile(metadataPath, renderMetadataFile(metadataEntries, surfaceEntries));

for (const [surface, operationIds] of Object.entries(surfaceEntries)) {
  const filePath = path.resolve(outputDir, `${surface}-client.ts`);
  await writeFile(filePath, renderClientFile(surface, operationIds));
}

await writeFile(indexPath, renderIndexFile());

function inferAuth(security) {
  if (!Array.isArray(security) || security.length === 0) {
    return "none";
  }

  for (const entry of security) {
    if (entry && typeof entry === "object") {
      if ("bearerAuth" in entry) {
        return "bearer";
      }

      if ("licenseAuth" in entry) {
        return "license";
      }
    }
  }

  return "none";
}

function extractSuccessResponses(responses) {
  const entries = [];

  for (const [status, response] of Object.entries(responses)) {
    const statusCode = Number(status);

    if (!Number.isInteger(statusCode)) {
      continue;
    }

    if (statusCode >= 200 && statusCode < 300) {
      entries.push({ statusCode, kind: responseKind(response) });
      continue;
    }

    if (isErrorResponse(response)) {
      continue;
    }

    entries.push({ statusCode, kind: responseKind(response) });
  }

  if (entries.length === 0) {
    throw new Error("Operation is missing a successful response");
  }

  entries.sort((left, right) => left.statusCode - right.statusCode);
  return entries;
}

function isErrorResponse(response) {
  if (!response || typeof response !== "object") {
    return false;
  }

  if (typeof response.$ref === "string") {
    return true;
  }

  const schema = response.content?.["application/json"]?.schema;
  return schema?.$ref === "#/components/schemas/ErrorEnvelope";
}

function responseKind(response) {
  if (!response || typeof response !== "object") {
    return "empty";
  }

  if (!response.content) {
    return "empty";
  }

  if (response.content["application/json"]) {
    return "json";
  }

  if (response.content["text/plain"]) {
    return "text";
  }

  throw new Error("Unsupported success response content type");
}

function renderMetadataFile(metadataEntries, surfaceEntries) {
  const metadataBody = metadataEntries
    .map((entry) => {
      const successBody = entry.successEntries
        .map((successEntry) => `      ${successEntry.statusCode}: ${JSON.stringify(successEntry.kind)}`)
        .join(",\n");

      return [
        `  ${JSON.stringify(entry.operationId)}: {`,
        `    method: ${JSON.stringify(entry.method)},`,
        `    path: ${JSON.stringify(entry.path)},`,
        `    auth: ${JSON.stringify(entry.auth)},`,
        "    success: {",
        successBody,
        "    } as const",
        "  }"
      ].join("\n");
    })
    .join(",\n");

  return `// Generated by scripts/generate-operations.mjs. Do not edit manually.

export const operationMetadata = {
${metadataBody}
} as const;

export const managementOperationIds = ${renderStringArray(surfaceEntries.management)} as const;
export const runtimeOperationIds = ${renderStringArray(surfaceEntries.runtime)} as const;
export const systemOperationIds = ${renderStringArray(surfaceEntries.system)} as const;
`;
}

function renderClientFile(surface, operationIds) {
  const className = `${capitalize(surface)}Client`;
  const optionsType =
    surface === "management"
      ? "ManagementClientOptions"
      : surface === "runtime"
        ? "RuntimeClientOptions"
        : "SystemClientOptions";
  const authConfig =
    surface === "management"
      ? '{ type: "bearer", value: options.token }'
      : surface === "runtime"
        ? '{ type: "license", value: options.licenseKey }'
        : '{ type: "none" }';
  const methodBlocks = operationIds
    .map(
      (operationId) => `  ${operationId}(...args: OperationRequestArgs<${JSON.stringify(operationId)}>) {
    return this.request(${JSON.stringify(operationId)}, args[0], args[1]);
  }`
    )
    .join("\n\n");
  const rawBlocks = operationIds
    .map(
      (operationId) => `    ${operationId}: (...args: OperationRequestArgs<${JSON.stringify(operationId)}>) =>
      this.requestRaw(${JSON.stringify(operationId)}, args[0], args[1])`
    )
    .join(",\n");

  return `// Generated by scripts/generate-operations.mjs. Do not edit manually.

import type {
  ${optionsType}
} from "../client-options.js";
import { OperationClient } from "../internal/operation-client.js";
import type { OperationRequestArgs } from "../operations.js";

export class ${className} extends OperationClient {
  readonly raw = {
${rawBlocks}
  };

  constructor(options: ${optionsType}) {
    super(options, ${authConfig});
  }

${methodBlocks}
}
`;
}

function renderIndexFile() {
  return `// Generated by scripts/generate-operations.mjs. Do not edit manually.

export { ManagementClient } from "./management-client.js";
export { RuntimeClient } from "./runtime-client.js";
export { SystemClient } from "./system-client.js";
`;
}

function renderStringArray(values) {
  return `[${values.map((value) => JSON.stringify(value)).join(", ")}]`;
}

function capitalize(value) {
  return value.charAt(0).toUpperCase() + value.slice(1);
}
